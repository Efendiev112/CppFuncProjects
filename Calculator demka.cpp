
#include <iostream>
using namespace std;
int main()
{
	//calculator (only output result + - * /
	float a, b;
	cout << "Input a ";
	cin >> a;
	cout << "Input b ";
	cin >> b;
	cout << a << "+" << b << "=" << a + b << endl;
	cout << a << "-" << b << "=" << a - b << endl;
	cout << a << "/" << b << "=" << a / b << endl;
	cout << a << "*" << b << "=" << a * b << endl;

 
}


